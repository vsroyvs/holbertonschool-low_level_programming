#include "3-calc.h"

/**
 * op_add - prints an additon of two numbers
 * @a: 1 numbers
 * @b: 2 numbers
 * Return: a + b.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - prints an subtraction of two numbers
 * @a: 1 numbers
 * @b: 2 numbers
 * Return: a - b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - prints an multiplication of two numbers
 * @a: 1 numbers
 * @b: 2 numbers
 * Return: a * b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - prints an division of two numbers
 * @a: 1 numbers
 * @b: 2 numbers
 * Return: a / b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - prints the remainder of the division of two numbers
 * @a: 1 numbers
 * @b: 2 numbers
 * Return: a mod b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
